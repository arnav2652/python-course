#New Line

print("Hello\nWorld")
# Output:
# Hello
# World

# Tab

print("Name\tAge")
print("John\t25")
# Output:
# Name    Age
# John    25

#Backslash

print("C:\\Users\\John")
# Output: C:\Users\John

#Double Quote

print("She said, \"Hi there!\"")
# Output: She said, "Hi there!"


#Single Quote

print('It\'s a nice day.')
# Output: It's a nice day.
